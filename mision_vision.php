                                <h2>Nuestra Misi&oacute;n</h2>
                                <p>El Instituto San José del Carmen inspirado en la misión evangelizadora de la iglesia católica y el carisma propio de la congregación de las Carmelitas de San José, ofrece educación integral y de calidad, a niños y jóvenes de la sociedad hondureña, que es a la vez portadora de valores profundamente morales, éticos y espirituales. Para lograrlo estimulados el perfeccionamiento de nuestro personal docente, las relaciones de trabajo armoniosas y promovemos sólidos principios de integridad, ética y equidad.</p>

                               <h2>Nuestra Visi&oacute;n</h2>
                               <p> Ser una comunidad educativa de excelencia, transmisora y portadora de valores humanos, cristianos y científicos. En el cual todo alumno egresado se forme e identifique como auténtico cristiano, ciudadano de principios y valores, que aporten a la sociedad justicia , amor y paz en el área donde se desenvuelvan , siendo con ello artífice de una nueva Honduras.</p>

                               <h2>Valores:</h2>

                                <p>Fe:
                                Creer para ver es la consigna, fortaleciendo la espiritualidad hacia la Plena confianza en un Dios Vivo , Real y Presente en la vida de cada ser humano.</p>

                                <p>Amor:
                                Es el valor humano que enmarca la vida de todo miembro de esta Comunidad Educativa. Más allá de ser un sentimiento, se define como una decisión, que se refleja en acciones concretas en el que hacer cotidiano de cada persona, caracterizadas por el respeto, la comunicación, la honestidad, la tolerancia y la solidaridad.</p>

                                <p>Respeto:
                                Es conocer, aceptar, entender y valorar la dignidad y autenticidad de cada ser, a partir de su individualidad, manejando los límites propios en relación los otros con miras a desarrollar una sana convivencia.</p>

                                <p>Comunicación:
                                Se define como el intercambio e interacción con otros a nivel expresivo (gestual, verbal o corporal), siendo fundamental el buen uso del lenguaje, el correcto vocabulario y el buen trato al expresar. Dentro de este proceso es fundamental aprender a escuchar, ser receptivo y crítico en lo positivo.</p>

                                <p>Responsabilidad:
                                Es el compromiso que se adquiere consigo mismo de manera autónoma, para lograr la satisfacción del deber cumplido, llegando a un sentido de participación en el que existe la voluntad para intervenir con sentimiento de pertenencia en las diferentes actividades institucionales.</p>

                                <p>Orden:
                                es el valor humano que conduce al cuidado de sí mismo (presentación personal –uniforme…) y sus elementos personales ( pupitre, cuadernos, materiales, etc.). De igual manera, al aprovechamiento de los diferentes espacios físicos y recursos de toda índole (aulas, muebles, zonas verdes, comedor, oficinas, transporte…). Esto es: “un lugar para cada cosa y cada cosa en su lugar”.</p>

                                <p>Disciplina:
                                este valor está definido en nuestra Institución mediante la frase: “un momento para cada cosa y cada cosa en su momento”. Lo anterior, permite a cada ser humano aprovechar y disfrutar con sentido tanto las oportunidades como las experiencias de vida.</p>

                                <p>Familia:
                                Se refiere al concepto de unión, apoyo, compañía fraternidad, compromiso y participación dentro del interior de la Comunidad Educativa proyectándose hacia el Municipio y el país.</p>
